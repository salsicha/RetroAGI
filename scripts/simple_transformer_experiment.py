import math
import torch
import torch.nn as nn
import torch.optim as optim
import numpy as np
import matplotlib.pyplot as plt
from tqdm import tqdm
import torch.nn.functional as F

# --------------------------------------------------------
# 1. Synthetic Data Generator
# --------------------------------------------------------
def generate_hierarchical_data(num_samples, seq_len_a, ratio_ab, ratio_bc, vocab_size):
    """
    Generates three levels of data:
    Seq A: Slow discrete sequence.
    Seq B: Medium discrete sequence.
    Seq C: Fast continuous sequence (controlled system).
    """
    seq_len_b = seq_len_a * ratio_ab
    seq_len_c = seq_len_b * ratio_bc
    
    X_A, Y_A = [], []
    X_B, Y_B = [], []
    X_C_in, Y_C_target = [], []
    
    for _ in range(num_samples):
        # Sequence A: standard progression
        start_a = np.random.randint(0, vocab_size)
        seq_a = [(start_a + i) % vocab_size for i in range(seq_len_a + 1)]
        X_A.append(seq_a[:-1])
        Y_A.append(seq_a[1:])
        
        # Sequence B: faster timescale progression
        start_b = np.random.randint(0, vocab_size)
        seq_b = [(start_b + i) % vocab_size for i in range(seq_len_b + 1)]
        X_B.append(seq_b[:-1])
        
        # Target B (implicit, not directly trained, used to generate C's parameters)
        y_b = []
        c_in_seq = []
        c_target_seq = []
        for j in range(seq_len_b):
            i = j // ratio_ab # Index of latest A token
            # Combine A and B to get a target concept
            concept = (seq_b[j+1] + seq_a[i+1]) % vocab_size
            y_b.append(concept)
            
            # The concept defines the true parameters for the controller
            w_true = np.sin(concept)
            b_true = np.cos(concept)
            
            # Sequence C: Generate fast inputs and targets for the controller
            for k in range(ratio_bc):
                x_val = np.random.randn()
                y_val = w_true * x_val + b_true
                c_in_seq.append(x_val)
                c_target_seq.append(y_val)
                
        Y_B.append(y_b)
        X_C_in.append(c_in_seq)
        Y_C_target.append(c_target_seq)
        
    return (torch.tensor(X_A, dtype=torch.long), torch.tensor(Y_A, dtype=torch.long),
            torch.tensor(X_B, dtype=torch.long), torch.tensor(Y_B, dtype=torch.long),
            torch.tensor(X_C_in, dtype=torch.float), torch.tensor(Y_C_target, dtype=torch.float))

# --------------------------------------------------------
# 2. Transformer Model Components
# --------------------------------------------------------
class PositionalEncoding(nn.Module):
    def __init__(self, d_model, max_len=5000):
        super().__init__()
        pe = torch.zeros(max_len, d_model)
        position = torch.arange(0, max_len, dtype=torch.float).unsqueeze(1)
        div_term = torch.exp(torch.arange(0, d_model, 2).float() * (-math.log(10000.0) / d_model))
        
        pe[:, 0::2] = torch.sin(position * div_term)
        pe[:, 1::2] = torch.cos(position * div_term)
        self.register_buffer('pe', pe.unsqueeze(0))

    def forward(self, x):
        x = x + self.pe[:, :x.size(1), :]
        return x

class AdaptiveController(nn.Module):
    """
    A simple closed-loop/adaptive controller. 
    It applies parameters generated by a higher level to the input.
    """
    def __init__(self):
        super().__init__()
        
    def forward(self, x_c, w, b):
        # x_c shape: [batch_size, seq_len_c]
        # w, b shape: [batch_size, seq_len_b]
        # We need to upsample w and b to match seq_len_c
        ratio_bc = x_c.size(1) // w.size(1)
        
        w_upsampled = w.repeat_interleave(ratio_bc, dim=1)
        b_upsampled = b.repeat_interleave(ratio_bc, dim=1)
        
        y_hat = w_upsampled * x_c + b_upsampled
        return y_hat

class HierarchicalAdaptiveModel(nn.Module):
    """
    A three-level hierarchy: 
    Transformer A (Top) -> Transformer B (Mid) -> Adaptive Controller (Bottom)
    """
    def __init__(self, vocab_size, d_model=64, nhead=4, num_layers=2):
        super().__init__()
        self.d_model = d_model
        
        self.embedding = nn.Embedding(vocab_size, d_model)
        self.pos_encoder = PositionalEncoding(d_model)
        
        # Stream A (Top Level)
        encoder_layers_A = nn.TransformerEncoderLayer(
            d_model=d_model, nhead=nhead, dim_feedforward=d_model*4, batch_first=True
        )
        self.transformer_A = nn.TransformerEncoder(encoder_layers_A, num_layers)
        self.fc_out_A = nn.Linear(d_model, vocab_size)
        
        # Stream B (Middle Level)
        decoder_layers_B = nn.TransformerDecoderLayer(
            d_model=d_model, nhead=nhead, dim_feedforward=d_model*4, batch_first=True
        )
        self.transformer_B = nn.TransformerDecoder(decoder_layers_B, num_layers)
        
        # The outputs of Stream B generate the parameters for the Adaptive Controller
        self.fc_controller_params = nn.Linear(d_model, 2) # w and b
        
        # Controller (Bottom Level)
        self.controller = AdaptiveController()

    def generate_causal_mask(self, sz):
        mask = (torch.triu(torch.ones(sz, sz)) == 1).transpose(0, 1)
        mask = mask.float().masked_fill(mask == 0, float('-inf')).masked_fill(mask == 1, float(0.0))
        return mask

    def generate_cross_causal_mask(self, sz_B, sz_A, ratio):
        """ Prevents Stream B from looking at Stream A's future predictions. """
        mask = torch.ones(sz_B, sz_A)
        for i in range(sz_B):
            for j in range(sz_A):
                if j <= i // ratio:
                    mask[i, j] = 0.0 # 0.0 means 'allow to attend' in PyTorch
        return mask.masked_fill(mask == 1, float('-inf'))

    def forward(self, src_A, src_B, src_C, criticism=None, tau=1.0):
        seq_len_A = src_A.size(1)
        seq_len_B = src_B.size(1)
        ratio_AB = seq_len_B // seq_len_A
        
        causal_mask_A = self.generate_causal_mask(seq_len_A).to(src_A.device)
        causal_mask_B = self.generate_causal_mask(seq_len_B).to(src_B.device)
        
        # --- Top Level: Transformer A ---
        x_A = self.embedding(src_A) * math.sqrt(self.d_model)
        x_A = self.pos_encoder(x_A)
        
        # Inject Criticism from the World Model / Critic feedback loop
        if criticism is not None:
            x_A = x_A + criticism
            
        hidden_A = self.transformer_A(x_A, mask=causal_mask_A)
        logits_A = self.fc_out_A(hidden_A)
        
        # Bridge A -> B (Gumbel-Softmax)
        probs_A = F.gumbel_softmax(logits_A, tau=tau, hard=True, dim=-1)
        pred_emb_A = torch.matmul(probs_A, self.embedding.weight)
        
        # --- Middle Level: Transformer B ---
        x_B = self.embedding(src_B) * math.sqrt(self.d_model)
        x_B = self.pos_encoder(x_B)
        
        # Stream B uses Cross-Attention to look at Stream A's predictions.
        # We use a custom memory mask to maintain strict temporal causality.
        cross_mask = self.generate_cross_causal_mask(seq_len_B, seq_len_A, ratio_AB).to(src_B.device)
        
        hidden_B = self.transformer_B(tgt=x_B, memory=pred_emb_A, tgt_mask=causal_mask_B, memory_mask=cross_mask)
        
        # Transformer B outputs the parameters for the controller
        controller_params = self.fc_controller_params(hidden_B)
        w_pred = controller_params[:, :, 0]
        b_pred = controller_params[:, :, 1]
        
        # --- Bottom Level: Adaptive Controller ---
        y_hat_C = self.controller(src_C, w_pred, b_pred)
        
        return logits_A, y_hat_C, w_pred, b_pred

class WorldModel(nn.Module):
    """Predicts the next state given current state and action."""
    def __init__(self):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(2, 32),
            nn.ReLU(),
            nn.Linear(32, 1)
        )
        
    def forward(self, state, action):
        # state, action shapes: [batch_size, seq_len_c]
        x = torch.stack([state, action], dim=-1) # [batch_size, seq_len_c, 2]
        return self.net(x).squeeze(-1) # [batch_size, seq_len_c]

class Critic(nn.Module):
    """Evaluates the predicted next state and outputs a criticism vector."""
    def __init__(self, seq_len_c, seq_len_a, d_model):
        super().__init__()
        self.seq_len_a = seq_len_a
        self.d_model = d_model
        
        self.net = nn.Sequential(
            nn.Linear(seq_len_c, 128),
            nn.ReLU(),
            nn.Linear(128, seq_len_a * d_model)
        )
        
    def forward(self, next_state_pred):
        # next_state_pred shape: [batch_size, seq_len_c]
        critique = self.net(next_state_pred)
        return critique.view(-1, self.seq_len_a, self.d_model)

class AgentWorldModelCritic(nn.Module):
    """
    Combines the Agent, World Model, and Critic.
    Executes a two-pass iterative refinement using criticism.
    """
    def __init__(self, vocab_size, seq_len_a, seq_len_c, d_model=64):
        super().__init__()
        self.agent = HierarchicalAdaptiveModel(vocab_size, d_model=d_model)
        self.world_model = WorldModel()
        self.critic = Critic(seq_len_c, seq_len_a, d_model)
        
    def forward(self, src_A, src_B, src_C, tau=1.0):
        # Pass 1: Agent acts without criticism
        logits_A1, actions1, w_1, b_1 = self.agent(src_A, src_B, src_C, criticism=None, tau=tau)
        
        # World Model predicts the next state based on those actions
        next_state_pred = self.world_model(src_C, actions1)
        
        # Critic evaluates the world model's prediction
        criticism = self.critic(next_state_pred)
        
        # Pass 2: Agent acts again, this time incorporating the criticism
        logits_A2, actions2, w_2, b_2 = self.agent(src_A, src_B, src_C, criticism=criticism, tau=tau)
        
        return actions1, next_state_pred, criticism, actions2, logits_A2, w_2, b_2

# --------------------------------------------------------
# 3. Testing and Visualization System
# --------------------------------------------------------
def train_and_evaluate():
    # Hyperparameters
    vocab_size = 20
    seq_len_A = 8
    ratio_AB = 2
    ratio_BC = 4
    batch_size = 32
    epochs = 60
    tau_start = 5.0
    tau_end = 0.1
    
    if torch.backends.mps.is_available():
        device = torch.device("mps")
    elif torch.cuda.is_available():
        device = torch.device("cuda")
    else:
        device = torch.device("cpu")

    print(f"Using device: {device}")
    
    # Create datasets
    train_XA, train_YA, train_XB, train_YB, train_XC, train_YC = generate_hierarchical_data(1000, seq_len_A, ratio_AB, ratio_BC, vocab_size)
    test_XA, test_YA, test_XB, test_YB, test_XC, test_YC = generate_hierarchical_data(200, seq_len_A, ratio_AB, ratio_BC, vocab_size)
    
    train_XA, train_YA = train_XA.to(device), train_YA.to(device)
    train_XB, train_YB = train_XB.to(device), train_YB.to(device)
    train_XC, train_YC = train_XC.to(device), train_YC.to(device)
    
    test_XA, test_YA = test_XA.to(device), test_YA.to(device)
    test_XB, test_YB = test_XB.to(device), test_YB.to(device)
    test_XC, test_YC = test_XC.to(device), test_YC.to(device)
    
    seq_len_C = seq_len_A * ratio_AB * ratio_BC
    
    # Initialize model, loss, optimizer
    model = AgentWorldModelCritic(vocab_size=vocab_size, seq_len_a=seq_len_A, seq_len_c=seq_len_C).to(device)
    criterion = nn.MSELoss()
    optimizer = optim.Adam(model.parameters(), lr=0.001)
    
    history = {'loss_agent1': [], 'loss_agent2': [], 'loss_wm': [], 'error_B': [], 'accuracy_A': []}
    
    print("\nStarting Hierarchical Adaptive Controller Training...")
    for epoch in tqdm(range(epochs), desc="Training Epochs"):
        # Linearly decay temperature from tau_start to tau_end over the epochs
        tau = max(tau_end, tau_start - (tau_start - tau_end) * (epoch / max(1, epochs - 1)))
        
        model.train()
        
        permutation = torch.randperm(train_XA.size()[0])
        epoch_loss_1 = 0
        epoch_loss_2 = 0
        epoch_loss_wm = 0
        
        for i in range(0, train_XA.size()[0], batch_size):
            indices = permutation[i:i+batch_size]
            batch_xa, batch_ya = train_XA[indices], train_YA[indices]
            batch_xb = train_XB[indices]
            batch_xc_in, batch_yc_target = train_XC[indices], train_YC[indices]
            
            optimizer.zero_grad()
            actions1, next_state_pred, criticism, actions2, logits_A2, w_2, b_2 = model(batch_xa, batch_xb, batch_xc_in, tau=tau)
            
            # 1. World Model Loss (Simulate environment transition S' = S + Action)
            true_next_state = batch_xc_in + actions1.detach()
            loss_wm = criterion(next_state_pred, true_next_state)
            
            # 2. Agent Pass 1 Loss (No criticism)
            loss_agent1 = criterion(actions1, batch_yc_target)
            
            # 3. Agent Pass 2 Loss (With criticism)
            # The Critic's gradients come entirely through this loss!
            loss_agent2 = criterion(actions2, batch_yc_target)
            
            # Total Loss
            loss = loss_agent1 + loss_agent2 + loss_wm
            loss.backward()
            optimizer.step()
            
            epoch_loss_1 += loss_agent1.item()
            epoch_loss_2 += loss_agent2.item()
            epoch_loss_wm += loss_wm.item()
            
        # Validation step
        model.eval()
        with torch.no_grad():
            # Evaluate with the lowest temperature to simulate hard discrete choices
            test_actions1, test_next_state, test_crit, test_actions2, test_logits_A2, w_pred, b_pred = model(test_XA, test_XB, test_XC, tau=tau_end)
            
            preds_A = test_logits_A2.argmax(dim=-1)
            acc_A = (preds_A == test_YA).sum().item() / test_YA.numel() * 100
            
            # Calculate Layer B's implicit parameter prediction error
            w_true = torch.sin(test_YB.float())
            b_true = torch.cos(test_YB.float())
            error_B = ((F.mse_loss(w_pred, w_true) + F.mse_loss(b_pred, b_true)) / 2).item()
            
        batches = train_XA.size()[0] / batch_size
        avg_loss_1 = epoch_loss_1 / batches
        avg_loss_2 = epoch_loss_2 / batches
        avg_loss_wm = epoch_loss_wm / batches
        
        history['loss_agent1'].append(avg_loss_1)
        history['loss_agent2'].append(avg_loss_2)
        history['loss_wm'].append(avg_loss_wm)
        history['error_B'].append(error_B)
        history['accuracy_A'].append(acc_A)
        
        if (epoch + 1) % 5 == 0:
            tqdm.write(f"Epoch {epoch+1:02d}/{epochs} [Tau: {tau:.2f}] - Agent Loss P1: {avg_loss_1:.4f} -> P2: {avg_loss_2:.4f} | WM Loss: {avg_loss_wm:.4f} | Param Err B: {error_B:.4f} | Acc A: {acc_A:.1f}%")
            
            # Show a sample prediction during training
            target_c = test_YC[0].cpu().numpy()
            pred_c = test_actions2[0].cpu().numpy()
            
            tqdm.write(f"   [Controller Target C] : {target_c[:5].round(2)}...")
            tqdm.write(f"   [Controller Pred C]   : {pred_c[:5].round(2)}...\n")

    # Visualization
    plt.figure(figsize=(24, 5))
    
    plt.subplot(1, 4, 1)
    plt.plot(history['loss_agent1'], label='Pass 1 (No Critic)', color='red', alpha=0.6)
    plt.plot(history['loss_agent2'], label='Pass 2 (With Critic)', color='blue')
    plt.title('Layer C: Controller Loss')
    plt.xlabel('Epochs')
    plt.ylabel('MSE Loss')
    plt.legend()
    
    plt.subplot(1, 4, 2)
    plt.plot(history['loss_wm'], label='World Model Loss', color='purple')
    plt.title('World Model Dynamics Loss')
    plt.xlabel('Epochs')
    plt.ylabel('MSE')
    plt.legend()
    
    plt.subplot(1, 4, 3)
    plt.plot(history['error_B'], label='Parameter Error B', color='orange')
    plt.title('Layer B: Param Prediction Error')
    plt.xlabel('Epochs')
    plt.ylabel('MSE (w, b)')
    plt.legend()
    
    plt.subplot(1, 4, 4)
    plt.plot(history['accuracy_A'], label='Accuracy A', color='green', linestyle='--')
    plt.title('Layer A: Concept Accuracy')
    plt.xlabel('Epochs')
    plt.ylabel('Accuracy (%)')
    plt.legend()
    
    plt.tight_layout()
    plt.show()

if __name__ == "__main__":
    train_and_evaluate()
